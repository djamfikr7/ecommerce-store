interface LocaleLayoutProps {
  children: React.ReactNode
  params: Promise<{ locale: string }>
}

export default async function LocaleLayout({ children, params }: LocaleLayoutProps) {
  const { locale } = await params
  const validLocale = locales.includes(locale as Locale) ? locale : defaultLocale

  const localeMessages = messages[validLocale as Locale] || messages.en

  return (
    <NextIntlClientProvider messages={localeMessages}>
      <LocaleProvider initialLocale={validLocale as Locale}>
        <CurrencyProvider>
          <div className="min-h-screen flex flex-col" lang={validLocale}>
            <Header />
            <main id="main-content" role="main" className="flex-1">
              {children}
            </main>
            <Footer />
          </div>
        </CurrencyProvider>
      </LocaleProvider>
    </NextIntlClientProvider>
  )
}

export function generateStaticParams() {
  return locales.map((locale) => ({ locale }))
}
